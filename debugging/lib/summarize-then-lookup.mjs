import { writeFileSync } from "node:fs";
import { createGateway, gateway } from "@ai-sdk/gateway";
import {
  buildSystemPrompt,
  buildUserPrompt,
} from "@gistdiff/core/prompt";
import { generateText } from "ai";
import { gitDiff, loadEnvLocal } from "./env.mjs";
import { serializeError } from "./errors.mjs";

/**
 * One-shot: call generateText, then immediately poll
 * gateway.getGenerationInfo with the resulting id. Writes both the
 * summarize response and the generation-info polling history to the
 * provided paths, overwriting the canonical sibling files.
 *
 * This is the path that reproduces NOTES.md 1.2's eventual-consistency
 * bug: because we call the lookup endpoint with zero settling time, the
 * first few attempts are expected to fail with
 * "Invalid error response format: Gateway request failed".
 */
export async function runSummarizeThenLookup({
  commit,
  model,
  description,
  note,
  summarizeOutPath,
  infoOutPath,
  exampleNumber,
  maxAttempts = 12,
  intervalMs = 1000,
}) {
  loadEnvLocal();

  const diff = gitDiff(commit);
  const system = buildSystemPrompt(description);
  const user = buildUserPrompt(diff);

  const summarizeStart = Date.now();
  const result = await generateText({
    model: gateway(model),
    messages: [
      { role: "system", content: system },
      { role: "user", content: user },
    ],
    providerOptions: {
      gateway: { caching: "auto" },
      anthropic: { thinking: { type: "adaptive" } },
      openai: { reasoningEffort: "low" },
      google: { thinkingLevel: "low" },
      bedrock: { reasoningConfig: { type: "adaptive" } },
    },
  });
  const summarizeWallMs = Date.now() - summarizeStart;

  const summarizeDump = {
    meta: {
      example: exampleNumber,
      model,
      description,
      diffCommit: commit,
      wallClockMs: summarizeWallMs,
      capturedAt: new Date().toISOString(),
      note: `${note} (generated by summarize-then-lookup.request.mjs)`,
    },
    text: result.text,
    reasoningText: result.reasoningText,
    finishReason: result.finishReason,
    usage: result.usage,
    warnings: result.warnings,
    response: result.response,
    request: result.request,
    providerMetadata: result.providerMetadata,
    content: result.content,
  };
  writeFileSync(summarizeOutPath, `${JSON.stringify(summarizeDump, null, 2)}\n`);
  console.log(`  [${model}] ${result.text.trim().split("\n")[0]}`);
  console.log(`  wrote ${summarizeOutPath}`);

  const generationId = result.providerMetadata?.gateway?.generationId;
  if (!generationId) {
    throw new Error("no generationId on providerMetadata.gateway");
  }

  const gw = createGateway({ apiKey: process.env.AI_GATEWAY_API_KEY });
  const attempts = [];
  for (let i = 1; i <= maxAttempts; i++) {
    const start = Date.now();
    try {
      const info = await gw.getGenerationInfo({ id: generationId });
      attempts.push({ attempt: i, elapsedMs: Date.now() - start, ok: true, info });
      console.log(`  attempt ${i}: OK after ${Date.now() - start}ms`);
      break;
    } catch (err) {
      attempts.push({
        attempt: i,
        elapsedMs: Date.now() - start,
        ok: false,
        error: serializeError(err),
      });
      console.log(
        `  attempt ${i}: ERR (${Date.now() - start}ms) — ${err?.message ?? err}`,
      );
      if (i < maxAttempts) await new Promise((r) => setTimeout(r, intervalMs));
    }
  }

  writeFileSync(
    infoOutPath,
    `${JSON.stringify(
      {
        meta: {
          generationId,
          capturedAt: new Date().toISOString(),
          maxAttempts,
          intervalMs,
          note: "Back-to-back reproduction: lookup called immediately after summarize completed. See NOTES.md 1.2.",
        },
        attempts,
      },
      null,
      2,
    )}\n`,
  );
  console.log(`  wrote ${infoOutPath}`);
}
